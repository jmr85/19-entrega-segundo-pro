const { Schema, model } = require("mongoose");
const productSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  stock: Number,
});

module.exports = model("Product", productSchema);
